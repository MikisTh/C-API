char var = 'b';

switch (var) {
  case 'a':
    cout << "Caso 1." << endl;
    break;
  case 'b':
    cout << "Caso 2." << endl;
    break;
  case 'c':
    cout << "Caso 3." << endl;
    break;
  case 'd':
    cout << "Caso 4." << endl;
    break;
  default:
    cout << "Caso 5." << endl;
    break;
}
